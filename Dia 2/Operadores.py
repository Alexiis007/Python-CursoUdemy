num1 = 1
num2 = 2

print(f"{num1} + {num2} es {num1+num2}")
print(f"{num1} - {num2} es {num1-num2}")
print(f"{num1} * {num2} es {num1*num2}")
print(f"{num1} / {num2} es {num1/num2}")
# Divicion aplanada o cencilla
print(f"{num1} // {num2} es {num1//num2}")
print(f"{num1} % {num2} es {num1%num2}")
# 1 a la 2 potencia
print(f"{num1} ** {num2} es {num1**num2}")
print(f"Raiz cuadrada de {num1} es {num1**0.5}")